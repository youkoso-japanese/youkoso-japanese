const $ = (sel, root = document) => root.querySelector(sel);
const $$ = (sel, root = document) => Array.from(root.querySelectorAll(sel));

function getLanguage() {
  return localStorage.getItem('yk_lang') || 'en';
}
function getTheme() {
  return localStorage.getItem('yk_theme') || 'dark';
}
function setTheme(theme) {
  document.body.classList.toggle('dark', theme === 'dark');
  localStorage.setItem('yk_theme', theme);
  const btn = $('#themeToggle');
  if (btn) btn.innerHTML = theme === 'dark' ? '☀' : '☾';
}
function setLanguage(lang) {
  localStorage.setItem('yk_lang', lang);
  document.documentElement.lang = lang === 'ja' ? 'ja' : (lang === 'si' ? 'si' : 'en');
  $$('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (translations[lang] && translations[lang][key] !== undefined) el.textContent = translations[lang][key];
  });
  $$('[data-i18n-html]').forEach(el => {
    const key = el.dataset.i18nHtml;
    if (translations[lang] && translations[lang][key] !== undefined) el.innerHTML = translations[lang][key];
  });
  $$('[data-i18n-placeholder]').forEach(el => {
    const key = el.dataset.i18nPlaceholder;
    if (translations[lang] && translations[lang][key] !== undefined) el.placeholder = translations[lang][key];
  });
  const langPicker = $('#langPicker');
  if (langPicker) langPicker.value = lang;
}

function showToast(message) {
  const toast = $('#toast');
  if (!toast) return;
  toast.textContent = message;
  toast.classList.add('show');
  clearTimeout(window.__toastTimer);
  window.__toastTimer = setTimeout(() => toast.classList.remove('show'), 2400);
}

function getEntries() {
  try { return JSON.parse(localStorage.getItem('yk_registrations') || '[]'); }
  catch { return []; }
}
function saveEntries(entries) {
  localStorage.setItem('yk_registrations', JSON.stringify(entries));
}
function addEntry(entry) {
  const entries = getEntries();
  entry.id = 'YK-' + Date.now().toString().slice(-8);
  entry.createdAt = new Date().toISOString();
  entry.status = 'new';
  entries.unshift(entry);
  saveEntries(entries);
}

function initNav() {
  const menu = $('#menuToggle');
  const nav = $('#navLinks');
  if (menu && nav) {
    menu.addEventListener('click', () => nav.classList.toggle('open'));
  }
  const currentPage = document.body.dataset.page;
  $$('[data-nav]').forEach(a => {
    if (a.dataset.nav === currentPage) a.classList.add('active');
  });
}

function initGallery() {
  const buttons = $$('.filter-btn');
  const items = $$('.gallery-item');
  if (!buttons.length || !items.length) return;
  buttons.forEach(btn => btn.addEventListener('click', () => {
    buttons.forEach(b => b.classList.remove('active'));
    btn.classList.add('active');
    const filter = btn.dataset.filter;
    items.forEach(item => {
      const show = filter === 'all' || item.dataset.category === filter;
      item.style.display = show ? '' : 'none';
    });
  }));
}

function initForms() {
  const forms = $$('form[data-store]');
  forms.forEach(form => {
    form.addEventListener('submit', (e) => {
      e.preventDefault();
      const data = Object.fromEntries(new FormData(form).entries());
      data.formType = form.dataset.store;
      addEntry(data);
      form.reset();
      showToast(translations[getLanguage()].toast_saved);
    });
  });
}

function renderAdmin() {
  const list = $('#adminList');
  const entries = getEntries();
  if (!list) return;
  const counts = {
    total: entries.length,
    registrations: entries.filter(e => e.formType === 'registration').length,
    visa: entries.filter(e => e.formType === 'visa').length,
    new: entries.filter(e => e.status === 'new').length,
  };
  const map = {
    total: '#countTotal',
    registrations: '#countReg',
    visa: '#countVisa',
    new: '#countNew'
  };
  Object.entries(map).forEach(([k, sel]) => {
    const el = $(sel);
    if (el) el.textContent = counts[k];
  });
  if (!entries.length) {
    list.innerHTML = `<div class="card"><h3>No records yet</h3><p>Registrations will appear here after form submissions.</p></div>`;
    return;
  }
  list.innerHTML = `
    <div class="card table-wrap">
      <table>
        <thead>
          <tr>
            <th>ID</th><th>Type</th><th>Name</th><th>Contact</th><th>Office</th><th>Status</th><th>Date</th>
          </tr>
        </thead>
        <tbody>
          ${entries.map(entry => `
            <tr>
              <td>${entry.id || ''}</td>
              <td>${entry.formType || ''}</td>
              <td>${entry.fullName || entry.name || ''}</td>
              <td>${entry.phone || entry.mobile || ''}</td>
              <td>${entry.office || ''}</td>
              <td><span class="status ${entry.status || 'new'}">${entry.status || 'new'}</span></td>
              <td>${new Date(entry.createdAt).toLocaleString()}</td>
            </tr>
          `).join('')}
        </tbody>
      </table>
    </div>`;
  const clearBtn = $('#clearRecords');
  if (clearBtn) clearBtn.onclick = () => {
    saveEntries([]);
    renderAdmin();
    showToast('Cleared');
  };
  const exportBtn = $('#exportRecords');
  if (exportBtn) exportBtn.onclick = () => {
    const blob = new Blob([JSON.stringify(entries, null, 2)], {type:'application/json'});
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url; a.download = 'youkoso-registrations.json'; a.click();
    URL.revokeObjectURL(url);
  };
}

function initTabs() {
  const tabs = $$('.tab[data-target]');
  tabs.forEach(tab => tab.addEventListener('click', () => {
    tabs.forEach(t => t.classList.remove('active'));
    tab.classList.add('active');
    const target = tab.dataset.target;
    $$('.tab-panel').forEach(panel => panel.hidden = panel.id !== target);
  }));
}

document.addEventListener('DOMContentLoaded', () => {
  setTheme(getTheme());
  setLanguage(getLanguage());
  initNav();
  initGallery();
  initForms();
  initTabs();
  renderAdmin();

  const themeToggle = $('#themeToggle');
  if (themeToggle) {
    themeToggle.addEventListener('click', () => {
      setTheme(document.body.classList.contains('dark') ? 'light' : 'dark');
    });
  }
  const langPicker = $('#langPicker');
  if (langPicker) {
    langPicker.addEventListener('change', (e) => setLanguage(e.target.value));
  }
});
